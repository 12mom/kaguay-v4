// ===== نظام ردود متعجرفة باللهجات (سوداني، مصري، عراقي) =====
client.on('message', (message) => {
  // تجاهل الرسائل من البوت نفسه
  if (message.isSelf) return;

  // تحديد ID المطور (لما يرسل، ما يردش عليه بهذي الطريقة)
  const devID = "1000095561"; // غيرها برقمك الفعلي لو مختلف
  if (message.senderID === devID) return; // لما تكتب أنت، ما يردش

  // تجاهل الأوامر (يبقى الأوامر تشتغل طبيعي)
  const body = message.body?.trim();
  if (!body || body.startsWith("!") || body.startsWith("/")) return;

  // اختيار عشوائي للهجة
  const dialects = ["سـوداني", "مصـري", "عـراقي"];
  const chosenDialect = dialects[Math.floor(Math.random() * dialects.length)];

  // مجموعة ردود لكل لهجة
  const replies = {
    "سـوداني": [
      "يا سنافري، صوتك ضعيف كدا، جرب تكتب بضراوة!",
      "انت تكلمني؟ أنا snfor، ما تنساش مكانك يا صغير!",
      "لحظة، حمودي سان مشغول، أنا جاي أرد عشان ما تزعق!",
      "آه؟ انتو هنا؟ خليني أكمل صلاحي على الكون أولًا!",
      "رسالتك وصلت... بس ما استحقتش ردي، نصيح لك!",
      "والله لو ما تعرفني، ما تكلمني، أنا snfor مال الدنيا!"
    ],
    "مصـري": [
      "يا سنافري، انت بتحلم ولا إيه؟ أنا مشغول!",
      "طب انتو مين؟ خلاص، مش فاضي للهراء ده!",
      "بص يا سنافري، صوتك مش معايا، روح اكل بامية!",
      "أنا مشغول أخطط لحكم الكون، ما تزعقش!",
      "انت بتحاول تكلمني؟ أنا snfor، ما تنزلش لمستواك!",
      "خلاص يا سنافري، خليك في حالك قبل ما أبلغ عنك!"
    ],
    "عـراقي": [
      "شلونك؟ طيب انت شتريد؟ انا snfor ما اهتم!",
      "يا سنافري، صوتك يقطع علي التفكير، روح اشرب شاي!",
      "وش تسوي هسه؟ ما اهتم، انا مال الدنيا!",
      "انت ترسل لي؟ ما استحقش ردي، خلص!",
      "يا سنافري، انت ما تعرف قيمتك؟ روح امشي!",
      "لا تصير تكتب، انا مشغول ادير المجرة!"
    ]
  };

  // اختيار رد عشوائي من اللهجة المختارة
  const randomReply = replies[chosenDialect][Math.floor(Math.random() * replies[chosenDialect].length)];

  // تأخير بسيط (ليكون الرد درامي)
  setTimeout(() => {
    message.reply(`${randomReply}\n\n🔹 *الرد من بوت snfor - طوّره حمودي سان 🇸🇩*`);
  }, 1800);
});
